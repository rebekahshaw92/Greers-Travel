<h2>Booking Details</h2>

<p>When a booking is made all details will be read back to you. Once you have confirmed these details we will proceed to confirm the booking with the principal(s) or supplier(s). Please check that all names, dates and timings are correct on receipt of all documents and advise us of any errors immediately. Any changes to these details will incur additional charges. Please ensure that the names given are the same as in the relevant passport. The booking information that you provide to us will be passed on only to the relevant suppliers of your travel arrangements or other persons necessary for the provision of your travel arrangements. The information may therefore be provided to public authorities such as customs or immigration if required by them, or as required by law. This applies to any sensitive information that you give to us such as details of any disabilities, or dietary and religious requirements. Certain information may also be passed on to security or credit checking companies. If you are travelling to the United States, the US Customs and Border Protection will receive this information for the purposes of preventing and combating terrorism and other transnational serious crimes. If you travel outside the European Economic Area, controls on data protection may not be as strong as the legal requirements in this country. If we cannot pass this information to the relevant suppliers, whether in the EEA or not, we will be unable to provide your booking. In making this booking, you consent to this information being passed on to the relevant persons. Full details of our data protection policy are available upon request. Your data controller is: Greer’s Travel Office Ltd – Registration Reference Z5120609.</p>

<h2>Payment</h2>

<p>You will be required to pay a deposit or make full payment for your booking at the time of booking. Where you only pay a deposit you must pay the full balance by the balance due date notified to you. If full payment is not received by the balance due date, we will notify the principal(s) or supplier(s) who may cancel your booking and charge the cancellation fees set out in their Terms and Conditions.</p>

<h2>Cancellation and Amendment</h2>

<p>Any cancellation or amendment request must be sent to us in writing and will not take effect until received by us. If you cancel or amend your booking the principal(s) or supplier(s) may charge the cancellation or amendment charge shown in their Terms and Conditions (which may be 100% of the cost of the travel arrangements) and you must pay us the relevant cancellation or amendment charge.</p>

<h2>Insurance</h2>

<p>Many principals/suppliers require you to take out travel insurance as a condition of booking with them. In any event, we strongly advise that you take out a policy of insurance in order to cover you and your party against the cost of cancellation by you; the cost of assistance (including repatriation) in the event of accident or illness; loss of baggage and money; and other expenses. You should check your policy carefully to ensure that all the details are correct and that all relevant information has been provided by you (e.g. pre-existing medical conditions). Failure to disclose relevant information will affect your insurance. Insurance against the financial failure of the principal(s)/ suppliers(s) of your travel arrangements is highly recommended where you would not otherwise be protected e.g. when you buy a no-frills flight. Further details will be provided at time of booking.</p>

<h2>Financial Protection</h2>

<p>All the package holidays we sell come with protection for your money. If you buy a single travel service then this might not apply. Package holidays are protected by the package organiser and we will provide you with their confirmation. When you buy an ATOL protected flight or flight inclusive holiday from us you will receive an ATOL Certificate. This lists what is financially protected, where you can get information on what this means for you and who to contact if things go wrong. If we, or the suppliers identified on your ATOL certificate, are unable to provide the services listed (or a suitable alternative, through an alternative ATOL holder or otherwise) for reasons of insolvency, the Trustees of the Air Travel Trust may make a payment to (or confer a benefit on) you under the ATOL scheme. You agree that in return for such a payment or benefit you assign absolutely to those Trustees any claims which you have or may have arising out of or relating to the non-provision of the services, including any claim against us, the travel agent (or your credit card issuer where applicable). You also agree that any such claims may be re-assigned to another body, if that other body has paid sums you have claimed under the ATOL scheme.</p>

<h2>Delivery of Documents</h2>

<p>All documents (e.g. invoices, tickets, Insurance policies) that require to be posted will be forwarded without delay to you. Once documents leave our offices we will not be responsible for their loss unless such loss is due to our negligence. You can ask for delivery by other means but this may be subject to additional charges.</p>

<h2>Passports, Visas and Health</h2>

<p> We can provide general information about the passport and visa requirements for your trip. Your specific passport and visa requirements, and other immigration requirements are your responsibility and you should confirm these with the relevant Embassies and/or Consulates. Neither we nor the principal(s) or supplier(s) accept any responsibility if you cannot travel because you have not complied with any passport, visa or immigration requirements. Most countries now require passports to be valid for at least 6 months after your return date. Please take special note that for all air travel within the British Isles, airlines require photographic identification of a specific type. Please ask us for full details. We can provide general information about any health formalities required for your trip but you should check with your own doctor for your specific circumstances.</p>

<h2>Final Travel Arrangements</h2>

<p>Please ensure that all your travel, passport, visa and insurance documents are in order and that you arrive in plenty of time for checking in at the airport. It may be necessary to reconfirm your flight with the airline prior to departure. Please ask us for details at least 72 hours before your outbound flight. You should take a note of any reference number or contact name when reconfirming. If you fail to reconfirm you may be refused permission to board the aircraft and you are unlikely to receive any refund.</p>

<h2>Complaints</h2>

<p> Because the contract(s) for your travel arrangements is between you and the principal(s) or supplier(s), any queries or concerns relating to the travel arrangements should be addressed to them. If you have a problem whilst on holiday, this must be reported to the principal/supplier or their local supplier or agent immediately. If you fail to follow this procedure there will be less opportunity to investigate and rectify your complaint. The amount of compensation you may be entitled to may be reduced or you may not receive any at all depending upon the circumstances. If you wish to complain when you return home, write to the principal/supplier. You will see the name and address plus contact details in any confirmation documents we send you. We will of course assist you with this if you wish - please contact Customer Services. If the matter cannot be resolved and it involves us or another ABTA Member then you have the option to use ABTA’s ADR scheme, approved by the Chartered Trading Standards Institute, see www.abta.com. You can also access the European Commission Online Dispute (ODR) Resolution platform at http://ec.europa.eu/consumers/odr/. This ODR platform is a means of registering your complaint with us; it will not determine how your complaint should be resolved.</p>

